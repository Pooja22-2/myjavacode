package basicsjava;
public class Assignment7_AB39_Arithematic_Operators 
{
	static void add() 
	{
		int a=10;
		int b=20;
		int add=a+b;
		System.out.println(add);
		
	}
	
	static void sub() 
	{
		int a=300;
		int b=200;
		int sub=a-b;
		System.out.println(sub);
		
	}
	
	static void mul() 
	{
		int a=100;
		int b=20;
		int mul=a*b;
		System.out.println(mul);
		
	}
	
	static void div() 
	{
		int a=200;
		int b=40;
		int div=a/b;
		System.out.println(div);
		
	}
	
	static void modulus() 
	{
		int a=200;
		int b=40;
		int modulus=a%b;
		System.out.println(modulus);
		
	}
	
	public static void main(String[] args) {
		add();
		sub();
		mul();
		div();
		modulus();
		
	}

}
